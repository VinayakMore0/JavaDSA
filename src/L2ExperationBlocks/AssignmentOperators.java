package L2ExperationBlocks;

public class AssignmentOperators {

    public static void main(String[] args) {

        // Assignment operators are used in Java to assign values to variables

         int age;
         age = 5;

         int a = 20;
         int val = age;

        System.out.println(val);

        val += a; // val = val + a;

        System.out.println(val);

        val -= a; // val = val - a;

        val *= a; // val = val * a;

        val /= a; // val = val / a;

        val %= a; // val = val % a;

    }
}
