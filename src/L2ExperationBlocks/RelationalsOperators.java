package L2ExperationBlocks;

public class RelationalsOperators {

    public static void main(String[] args) {

        //Relational Operators are used to check the relationship between two operators.

        //Relational operators will return true or false

        int a = 8, b = 20;

        System.out.println(a + " " + b);

        System.out.println(a == b); // is Equal to

        System.out.println(a != b); // is Not Equal to

        System.out.println(a > b);

        System.out.println(a < b);

        System.out.println(a <= b);

        System.out.println(a >= b);
    }
}
